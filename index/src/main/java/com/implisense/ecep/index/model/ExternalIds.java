package com.implisense.ecep.index.model;

public class ExternalIds {
    private String vat;
    private String ebid;

    public ExternalIds() {
    }

    public String getVat() {
        return vat;
    }

    public void setVat(String vat) {
        this.vat = vat;
    }

    public String getEbid() {
        return ebid;
    }

    public void setEbid(String ebid) {
        this.ebid = ebid;
    }
}
